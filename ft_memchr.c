#include "libft.h"

void	*ft_memchr(void *str, int c, size_t l)
{
	size_t i;

	i = 0;
	while (i < l)
	{
		if (*(char*)(str + i) == c)
			return (&str[i]);
		i++;
	}
	return (0);
}
