#include "libft.h"

char	*ft_strnstr(char *s1, char *s2, size_t l)
{
	int i;
	int j;

	i = 0;
	while (s1[i] != '\0')
	{
		j = 0;
		while (j + i < (int)l && s2[j] && s1[i + j] == s2[j])
			j++;
		if (s2[j] == '\0')
			return (&s1[i]);
		i++;
	}
	return (0);
}
